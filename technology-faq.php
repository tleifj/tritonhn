      <p><strong>How does the Triton production process work?</strong></p>
      <p>Triton enhances algal genomes, creating strains that produce high levels of valuable and useful natural proteins. Many of these are proteins cannot be produced in other organisms (such as bacteria and yeast), and others cannot be produced economically and at scale in those organisms that can make them (such as CHO cells).</p>
      <p>Triton’s approach enables production of a large number of these high-value proteins at comparatively low cost.</p>
      <p>This method of engineering algae is similar to that of engineering bacteria or animal cells. In sum, Triton uses the same basic tools and approaches developed over the past four decades for use in other traditional systems; however Triton’s scientists have spent more than 25 years learning how to apply these tools to algae. This approach enables Triton to rapidly create new strains of algae that produce a variety of proteins.  Triton holds extensive intellectual property on the specific processes and methods for enhancing algae in this manner, as well as for each product it has developed.</p>
      
